import type { Obstacle } from '../domain/obstacle'
import type { CaptureZone } from '../domain/capture-zone'

export type MapData = {
  captureZones: CaptureZone[]
  walls: Obstacle[]
  obstacles: Obstacle[]
}

export type MapJson = {
  width: number
  height: number
  walls: [number, number][]
  zones: [number, number][]
  obstacles: [number, number][]
}

function obstaclesFromTiles(
  width: number,
  height: number,
  tiles: [number, number][],
): Obstacle[] {
  const tileSet = new Set(tiles.map(([x, y]) => `${x},${y}`))
  const walkable: boolean[][] = Array.from({ length: height }, (_, y) =>
    Array.from({ length: width }, (_, x) => !tileSet.has(`${x},${y}`)),
  )
  const visited: boolean[][] = Array.from({ length: height }, () =>
    Array.from({ length: width }, () => false),
  )
  const obstacles: Obstacle[] = []
  for (let y = 0; y < height; y++) {
    for (let x = 0; x < width; x++) {
      if (walkable[y]![x] || visited[y]![x]) continue
      let w = 0
      while (x + w < width && !walkable[y]![x + w] && !visited[y]![x + w]) w++
      let h = 1
      while (y + h < height) {
        let fits = true
        for (let dx = 0; dx < w; dx++) {
          if (walkable[y + h]![x + dx] || visited[y + h]![x + dx]) { fits = false; break }
        }
        if (!fits) break
        h++
      }
      for (let dy = 0; dy < h; dy++)
        for (let dx = 0; dx < w; dx++)
          visited[y + dy]![x + dx] = true
      obstacles.push({ x, y, width: w, height: h })
    }
  }
  return obstacles
}

export function ingestMap(json: MapJson): MapData {
  const walls = obstaclesFromTiles(json.width, json.height, json.walls)
  const obstacles = obstaclesFromTiles(json.width, json.height, json.obstacles)
  const captureZones: CaptureZone[] = json.zones.length > 0
    ? [{ tiles: json.zones.map(([x, y]) => ({ x, y })) }]
    : []
  return { walls, obstacles, captureZones }
}

